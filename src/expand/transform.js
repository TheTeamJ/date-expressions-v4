// fを数値配列で挟んでいる時、fに隣接する数値配列の大小関係は xL =< xR である
// Valid
// - y: [2015, 2016], m: f, d: f, h: [11, 13]
//     - 2015/*/* 11:00 ~ 13:00
//     - 2016/*/* 11:00 ~ 13:00
// - y: [2015, 2016], m: [2, 4], d: f, h: [11, 13]
//     - 2015/3/* 11:00 ~ 13:00
//     - 2015/4/* 11:00 ~ 13:00
//     - 2015/5/* 11:00 ~ 13:00
//     - 2016/3/* 11:00 ~ 13:00
//     - 2016/4/* 11:00 ~ 13:00
//     - 2016/5/* 11:00 ~ 13:00
// - y: [2015, 2016], m: f, d: [19, 21], h: [11, 13]
//     - 2015/*/19 11:00 ~ 13:00
//     - 2015/*/20 11:00 ~ 13:00
//     - 2015/*/21 11:00 ~ 13:00
//     - 2016/*/19 11:00 ~ 13:00
//     - 2016/*/20 11:00 ~ 13:00
//     - 2016/*/21 11:00 ~ 13:00
// Invalid
// - y: [2015, 2016], m: f, d: f, h: [13, 11]
// - y: [2015, 2016], m: f, d: [21, 19], h: f
//    - mをfで展開する場合、先月の21日から19日のような表現はできない

